package Prosjekt;

public class Person {

String navn;
int antallKroner;
    

    public Person(String navn, int antallKroner) {

        this.navn = navn;
        
        this.antallKroner = antallKroner;

    }


    public String getNavn() {
        return navn;
    }


    public int getAntallKroner() {
        return antallKroner;


    }

    

    

}

    
